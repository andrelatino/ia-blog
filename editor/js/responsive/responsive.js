function clickResizerLink() {
    // Selecciona el enlace con la clase específica
    const resizerLink = document.getElementById('responsive');

    // Verifica si el enlace existe
    if (resizerLink) {
        // Simula un clic en el enlace
        resizerLink.click();
    } else {
        console.log('El enlace con la clase especificada no se encontró.');
    }
}


